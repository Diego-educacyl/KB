     private JTable tablaProductos;
    private DefaultTableModel modeloTabla;

    /**
     * Creates new form ProductosFrame
     */
    public ProductosFrame() {
      setTitle("GestiÃ³n de Productos");
        setSize(600, 400);
        setDefaultCloseOperation(DISPOSE_ON_CLOSE);
        setLocationRelativeTo(null);

        modeloTabla = new DefaultTableModel(new Object[]{"ID", "Nombre", "Precio", "Stock"}, 0);
        tablaProductos = new JTable(modeloTabla);
        JScrollPane scrollPane = new JScrollPane(tablaProductos);

        JButton btnInsertar = new JButton("Insertar");
        JButton btnModificar = new JButton("Modificar");
        JButton btnEliminar = new JButton("Eliminar");

        btnInsertar.addActionListener(e -> insertarProducto());
        btnModificar.addActionListener(e -> modificarProducto());
        btnEliminar.addActionListener(e -> eliminarProducto());

        JPanel panelBotones = new JPanel();
        panelBotones.add(btnInsertar);
        panelBotones.add(btnModificar);
        panelBotones.add(btnEliminar);

        add(scrollPane, BorderLayout.CENTER);
        add(panelBotones, BorderLayout.SOUTH);

        cargarProductos();
        setVisible(true);
    }

    private void cargarProductos() {
        modeloTabla.setRowCount(0);
        List<Producto> productos = ProductoDAO.obtenerTodos();
        for (Producto p : productos) {
            modeloTabla.addRow(new Object[]{p.getId(), p.getNombre(), p.getPrecio(), p.getStock()});
        }
    }

    private void insertarProducto() {
        String nombre = JOptionPane.showInputDialog(this, "Nombre:");
        double precio = Double.parseDouble(JOptionPane.showInputDialog(this, "Precio:"));
        int stock = Integer.parseInt(JOptionPane.showInputDialog(this, "Stock:"));

        Producto p = new Producto(0, nombre, precio, stock);
        ProductoDAO.insertar(p);
        cargarProductos();
    }

    private void modificarProducto() {
        int fila = tablaProductos.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona un producto");
            return;
        }

        int id = (int) modeloTabla.getValueAt(fila, 0);
        String nombre = JOptionPane.showInputDialog(this, "Nombre:", modeloTabla.getValueAt(fila, 1));
        double precio = Double.parseDouble(JOptionPane.showInputDialog(this, "Precio:", modeloTabla.getValueAt(fila, 2)));
        int stock = Integer.parseInt(JOptionPane.showInputDialog(this, "Stock:", modeloTabla.getValueAt(fila, 3)));

        Producto p = new Producto(id, nombre, precio, stock);
        ProductoDAO.actualizar(p);
        cargarProductos();
    }

    private void eliminarProducto() {
        int fila = tablaProductos.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona un producto");
            return;
        }

        int id = (int) modeloTabla.getValueAt(fila, 0);
        ProductoDAO.eliminar(id);
        cargarProductos();
    }


